package com.gus.comms.xml;


public class Boo {

	private int no;
	private String bname;

	public Boo() {

	}

	public Boo(int no, String bname) {
		super();
		this.no = no;
		this.bname = bname;
	}

	public int getNo() {
		return no;
	}

	public void setNo(int no) {
		this.no = no;
	}

	public String getBname() {
		return bname;
	}

	public void setBname(String bname) {
		this.bname = bname;
	}

}
